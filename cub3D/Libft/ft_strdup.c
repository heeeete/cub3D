#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int			i;
	const int	len = ft_strlen(s1);
	char		*dest;

	i = 0;
	dest = (char *)malloc(sizeof(char) * (len + 1));
	if (!dest)
		return (0);
	while (s1[i])
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
